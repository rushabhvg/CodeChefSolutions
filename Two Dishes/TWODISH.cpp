#include <bits/stdc++.h>
using namespace std;

#define ll long long int

int main() {
    ll t;
    cin>>t;
    while(t--) {
        ll n, a, b, c;
        cin>>n>>a>>b>>c;
        if ( (b>=n) && ((a+c)>=n) ) cout<<"Yes";
        else cout<<"No";
        cout<<endl;
    }
	return 0;
}

