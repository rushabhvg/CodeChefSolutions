#include <bits/stdc++.h>
using namespace std;

#define ll long long int

int main() {
    ll t;
    cin>>t;
    while(t--) {
        ll n;
        cin>>n;
        if (n==1) cout<<1;
        else if (n==3) cout<<2;
        else cout<<(n/2)+1;
        cout<<endl;
    }
	return 0;
}

