#include <bits/stdc++.h>
using namespace std;

#define ll long long int
#define vll std::vector<long long int>

void solve() {
    ll n;
    cin>>n;
    if(n==1) {
        cout<<1<<" "<< 1 <<"\n";
        return;
    }
    cout<<(n+1)/2<<" "<<n<<"\n";
}

int main() {
    ios_base::sync_with_stdio(false);cin.tie(NULL);cout.tie(NULL);
    int t = 0; cin>>t;
    while (t--) {
        solve();
    }
}

