#include <bits/stdc++.h>
using namespace std;

void solve() {
    int x, y;
    cin>>x>>y;
    
    int mx = x;
    if(mx<y) mx = y;
    cout<<(mx*2)-1<<endl;
    
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 0; cin>>t;
    while (t--) {
        solve();
    }
}

